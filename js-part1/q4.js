// Q.4 What are the major features introduced in ECMAScript 6?

/*
Ans: 

Arrow Functions: Arrow functions provide a more concise syntax for writing function expressions. They have lexical scoping of "this" and automatically bind to the surrounding context.

Classes: ECMAScript 6 introduced class syntax, allowing developers to define classes and utilize object-oriented programming concepts such as inheritance, constructors, and static methods.

Template Literals: Template literals allow for easy string interpolation and multiline strings. They use backticks (`) instead of single or double quotes and support placeholders for variable substitution.

Destructuring Assignment: Destructuring assignment enables extracting values from arrays or objects into individual variables. It provides a concise way to assign values and access nested data structures.


Promises: Promises provide a standardized way to handle asynchronous operations. 

Let and Const: ES6 introduced block-scoped variables using the let and const keywords. let allows the declaration of variables with block scope.

*/